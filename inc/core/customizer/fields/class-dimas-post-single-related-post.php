/**
 * Post Single Related Post class
 */
class Post_Single_Related_Post_Fields {
	/**
	 * Instance
	 *
	 * @var $instance
	 */
	protected static $instance = null;

	/**
	 * Initiator
	 *
	 * @since 1.0.0
	 * @return object
	 */
	public static function instance() {
		if ( is_null( self::$instance ) ) {
			self::$instance = new self();
		}

		return self::$instance;
	}

	/**
	 * Section name.
	 *
	 * @var string
	 */
	private static $section = 'post_single_related';

	/**
	 * Get customize fields
	 *
	 * @since 1.0.0
	 *
	 * @return array
	 */
	public static function get_fields() {

		$fields = array(

			// Post Single Related Post.
			'post_single_related_show' => array(
				'type'    => 'toggle',
				'label'   => esc_html__( 'Enable/Disable Single Related Post', 'dimas' ),
				'section' => self::$section,
				'default' => 1,
			),

		);

		return $fields;
	}

}
